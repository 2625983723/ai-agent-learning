@tool
def check_patent_timeline(patent_type: str, stage: str = "全流程") -> str:
    """
    查询专利各阶段的预计时间线。

    Args:
        patent_type: 专利类型（发明/实用新型/外观设计）
        stage: 查询的具体阶段（全流程/审查中/已授权）

    Returns:
        时间线说明字符串
    """
    timelines = {
        "发明专利": {
            "全流程": "申请→初审(1-3月)→公布(申请即公布)→实审请求→实审(12-18月)→授权/驳回",
            "审查中": "实审阶段通常12-18个月，期间可能收到审查意见通知书",
            "已授权": "授权后需缴授权当年年费，每年按时缴年费维持有效",
        },
        "实用新型": {
            "全流程": "申请→初审(3-6月)→授权/驳回（无实审）",
            "审查中": "仅形式审查，通常3-6个月完成",
            "已授权": "授权后需按时缴年费",
        },
        "外观设计": {
            "全流程": "申请→初审(3-6月)→授权/驳回",
            "审查中": "仅形式审查，通常3-6个月",
            "已授权": "授权后需按时缴年费",
        },
    }

    type_data = timelines.get(patent_type, {})
    return type_data.get(stage, f"未找到'{stage}'对应的时间信息。可用阶段: {list(type_data.keys())}")


@tool
def compare_patent_types(type_a: str, type_b: str) -> str:
    """
    对比两种专利类型的保护对象、期限、费用等差异。

    Args:
        type_a: 第一种专利类型
        type_b: 第二种专利类型

    Returns:
        对比结果字符串
    """
    comparison = {
        ("发明专利", "实用新型"): (
            "【发明 vs 实用新型】\n"
            "1. 审查方式: 发明需实审（18-24月），实用新型仅初审（6-8月）\n"
            "2. 保护期限: 发明20年，实用新型10年\n"
            "3. 保护范围: 发明保护产品/方法，实用新型仅保护产品构造\n"
            "4. 授权难度: 发明难度大（新颖性+创造性），实用新型相对容易\n"
            "5. 费用: 发明约5400+，实用新型约2000+"
        ),
        ("发明专利", "外观设计"): (
            "【发明 vs 外观设计】\n"
            "1. 保护对象: 发明保护技术方案，外观设计保护富有美感的设计\n"
            "2. 保护期限: 发明20年，外观设计15年\n"
            "3. 审查方式: 发明需实审，外观设计仅初审\n"
            "4. 费用: 发明约5400+，外观设计约2000+"
        ),
        ("实用新型", "外观设计"): (
            "【实用新型 vs 外观设计】\n"
            "1. 保护对象: 实用新型保护构造，外观设计保护外观\n"
            "2. 保护期限: 实用新型10年，外观设计15年\n"
            "3. 审查方式: 均仅需初审（6-8月 vs 4-6月）\n"
            "4. 费用: 两者相近，约2000-3500元"
        ),
    }

    # 双向查找
    result = comparison.get((type_a, type_b))
    if not result:
        result = comparison.get((type_b, type_a))
    if not result:
        result = f"暂不支持'{type_a}'和'{type_b}'的直接对比。\n可用对比: 发明vs实用新型、发明vs外观设计、实用新型vs外观设计"
    return result


@tool
def get_patent_checklist(action: str) -> list[str]:
    """
    获取专利申请前/后注意事项清单。

    Args:
        action: 操作类型，可选值：申请前、申请中、授权后

    Returns:
        注意事项清单列表
    """
    checklists = {
        "申请前": [
            "✅ 进行专利检索，确认无相同或近似现有技术",
            "✅ 准备完整技术交底书（技术领域、背景、发明内容、附图）",
            "✅ 确认发明人名单和申请人信息",
            "✅ 评估是否委托代理机构（建议首次申请委托）",
            "✅ 预估费用预算（官费+代理费）",
        ],
        "申请中": [
            "📋 保留申请日和申请号凭证",
            "📋 跟踪审查进度（CPC系统或代理机构反馈）",
            "📋 收到审查意见通知书后，在指定期限内答复（通常4个月）",
            "📋 如需修改申请文件，注意不超出原说明书范围",
        ],
        "授权后": [
            "📋 收到授权通知书后，在规定期限内缴纳授权当年年费",
            "📋 每年按时缴纳年费，避免专利权终止",
            "📋 如他人侵权，可依法维权（发律师函/行政投诉/诉讼）",
            "📋 专利权可转让、许可、质押",
        ],
    }
    return checklists.get(action, ["未知操作类型，可选：申请前/申请中/授权后"])


